package Streams;

public class DesafioFilter2 {
    final String nome;
    final Double preco;
    final Double desconto;
    final boolean freteGratis;
    public DesafioFilter2(String nome, Double preco, Double desconto, boolean freteGratis) {
        this.nome = nome;
        this.preco = preco;
        this.desconto = desconto;
        this.freteGratis = freteGratis;
    }
    
}
